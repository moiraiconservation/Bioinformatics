function CSV() {
	////////////////////////////////////////////////////////////////////////
	// METHOD //////////////////////////////////////////////////////////////
	this.parse = (str) => {
		const obj = {
			command: 'parse_csv',
			success: true,
			data: []
		}
		let lines = str.split(/\r?\n/);
		for (let i = 0; i < lines.length; i++) {
			let data = lines[i].split(',');
			obj.data.push(data);
		}
		return obj;
	}
//////////////////////////////////////////////////////////////////////////
// METHOD ////////////////////////////////////////////////////////////////
	this.parse_to_list = (str) => {
		const obj = {
			command: 'parse_csv_to_list',
			success: true,
			data: []
		}
		let lines = str.split(/\r?\n/);
		for (let i = 0; i < lines.length; i++) {
			let data = lines[i].split(',');
			for (let j = 0; j < data.length; j++)
			obj.data.push(data[j]);
		}
		return obj;
	}
//////////////////////////////////////////////////////////////////////////
}
